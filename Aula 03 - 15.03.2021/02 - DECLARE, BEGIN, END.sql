SET SERVEROUTPUT ON

--EXEMPLO 1
DECLARE
    AV1 NUMBER(4,2); --0.00 A 10.00
    AV2 NUMBER(4,2); --0.00 A 10.00
    MEDIA NUMBER(4,2); --0.00 A 10.00
    NOME VARCHAR2(30); --NOME DO ALUNO
BEGIN
    NOME := '&NOME_ALUNO';
    AV1 := &AV1_ALUNO;
    AV2 := &AV2_ALUNO;
    MEDIA := (AV1+AV2)/2;
    
    DBMS_OUTPUT.PUT_LINE('A MEDIA DO ALUNO ' || NOME || ' FOI DE: ' || MEDIA);
END;


--EXEMPLO 2
DECLARE
    PESO NUMBER(5,2); --999.99
    ALTURA NUMBER(3,2); --9.99
    IMC NUMBER(10,2); --99999999.99
BEGIN
    PESO := &PESO;
    ALTURA := &ALTURA;
    IMC := PESO/(ALTURA*ALTURA);
    
    DBMS_OUTPUT.PUT_LINE('IMC: ' || IMC);
END;
